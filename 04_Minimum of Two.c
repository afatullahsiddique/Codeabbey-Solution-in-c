#include <stdio.h>

int main ()
{
    int t, n;
    double div = 0, sum =0;

    printf("Number of integers:\n");
    scanf("%d", &t);

    printf("Enter the numbers:\n");
    for (int t1=1; t1 <= t; t1++)
    {

        scanf("%d",&n);
        sum = sum + n;
        div = sum / t;
    }

    printf("Average is \n%.2lf", div);
}






